// Дано произвольное число с плавающей точкой.
// Реализовать методы округления данного числа до ближайшего меньшего, большего аргумента и ближайшего целого числа.

let myNumber2: number = 100.9290857209857;

console.log(Math.floor(myNumber2));   // до ближайшего меньшего аргумента
console.log(Math.ceil(myNumber2));   // до ближайшего большего аргумента
console.log(Math.round(myNumber2)); // до ближайшего целого числа